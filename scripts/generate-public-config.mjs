/**
 * Writes public/config.js from .env (anon key only — safe for zazu.html).
 *
 * Usage: npm run config
 */

import { writeFileSync, mkdirSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import dotenv from 'dotenv';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

dotenv.config({ path: resolve(root, '.env') });

const url = process.env.SUPABASE_URL;
const anonKey = process.env.SUPABASE_ANON_KEY;

if (!url || !anonKey || url.includes('YOUR_PROJECT')) {
  console.warn(
    '[Zazu] SUPABASE_URL / SUPABASE_ANON_KEY not set — skipping public/config.js (demo words only).',
  );
  process.exit(0);
}

const outDir = resolve(root, 'public');
mkdirSync(outDir, { recursive: true });

const content = `// Auto-generated by npm run config — do not commit (see .gitignore)
window.ZAZU_CONFIG = {
  supabaseUrl: ${JSON.stringify(url)},
  supabaseAnonKey: ${JSON.stringify(anonKey)},
};
`;

const outPath = resolve(outDir, 'config.js');
writeFileSync(outPath, content, 'utf8');
console.log(`Wrote ${outPath}`);

const mobileEnv = `# Auto-generated by npm run config — do not commit
EXPO_PUBLIC_SUPABASE_URL=${url}
EXPO_PUBLIC_SUPABASE_ANON_KEY=${anonKey}
`;

const mobileEnvPath = resolve(root, 'mobile', '.env');
writeFileSync(mobileEnvPath, mobileEnv, 'utf8');
console.log(`Wrote ${mobileEnvPath}`);
